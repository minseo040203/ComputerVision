import cv2                 # OpenCV 라이브러리 → 이미지 처리, 영상 처리, GUI 창 생성 등에 사용
import os                  # 운영체제(OS) 관련 기능 → 파일 경로 처리, 파일 존재 여부 확인 등에 사용

# ---------------------------------------------------------
# 전역 변수 선언
# ---------------------------------------------------------

# ROI(Region Of Interest) 선택 시작 좌표
# 마우스를 처음 클릭한 위치가 저장된다
x_start, y_start = 0, 0

# ROI 선택 끝 좌표
# 마우스를 드래그 후 놓은 위치가 저장된다
x_end, y_end = 0, 0

# 현재 마우스로 드래그 중인지 여부
# True  → 사용자가 마우스를 누르고 드래그 중
# False → 드래그 중이 아님
drawing = False

# 현재 화면에 표시될 이미지
# 사용자가 ROI를 선택하면 사각형이 이 이미지 위에 그려진다
img = None

# 원본 이미지 복사본
# 사용자가 'r' 키를 눌렀을 때 초기 상태로 되돌리기 위해 사용
img_copy = None

# 선택된 ROI 영역을 저장할 변수
# ROI = Region Of Interest (관심 영역)
roi = None


# ---------------------------------------------------------
# 마우스 이벤트 처리 함수
# ---------------------------------------------------------
# OpenCV에서는 마우스 클릭, 이동, 드래그 같은 이벤트를
# 콜백(callback) 함수로 처리한다
# ---------------------------------------------------------

def mouse_event(event, x, y, flags, param):
    """
    마우스 이벤트 처리 함수
    사용자가 마우스로 ROI 영역을 드래그하여 선택할 수 있도록 한다.
    
    event : 발생한 마우스 이벤트 종류
    x, y  : 현재 마우스 좌표
    flags : 추가 이벤트 정보
    param : 추가 파라미터
    """

    # 전역 변수 사용 선언
    global x_start, y_start, x_end, y_end, drawing, img, img_copy, roi

    # -------------------------------------------------
    # 1️⃣ 마우스 왼쪽 버튼을 눌렀을 때
    # -------------------------------------------------
    if event == cv2.EVENT_LBUTTONDOWN:

        drawing = True             # 드래그 시작 상태

        # ROI 시작 좌표 저장
        x_start, y_start = x, y

        # 이전에 그려진 사각형을 지우기 위해
        # 원본 이미지를 복사하여 초기화
        img = img_copy.copy()


    # -------------------------------------------------
    # 2️⃣ 마우스를 움직일 때
    # -------------------------------------------------
    elif event == cv2.EVENT_MOUSEMOVE:

        # 현재 드래그 중이라면
        if drawing:

            # 매번 새롭게 사각형을 그리기 위해
            # 원본 이미지를 복사
            img = img_copy.copy()

            # ROI 선택 영역을 표시하기 위한 사각형 그리기
            cv2.rectangle(
                img,                        # 사각형을 그릴 이미지
                (x_start, y_start),         # 사각형 시작 좌표
                (x, y),                     # 현재 마우스 위치
                (0, 255, 0),                # 색상 (초록색) BGR 형식
                2                           # 선 두께
            )


    # -------------------------------------------------
    # 3️⃣ 마우스 왼쪽 버튼을 뗐을 때
    # -------------------------------------------------
    elif event == cv2.EVENT_LBUTTONUP:

        drawing = False            # 드래그 종료

        # ROI 끝 좌표 저장
        x_end, y_end = x, y

        # -------------------------------------------------
        # 좌표 정렬
        # -------------------------------------------------
        # 사용자가 드래그를
        # 왼쪽→오른쪽
        # 오른쪽→왼쪽
        # 위→아래
        # 아래→위
        # 어떤 방향으로 하든 정상 작동하도록
        # 좌표를 정렬해준다

        x1 = min(x_start, x_end)
        y1 = min(y_start, y_end)
        x2 = max(x_start, x_end)
        y2 = max(y_start, y_end)

        # -------------------------------------------------
        # ROI 영역 추출
        # -------------------------------------------------
        # NumPy 슬라이싱을 이용해
        # 이미지에서 특정 영역만 잘라낸다

        roi = img_copy[y1:y2, x1:x2].copy()

        # -------------------------------------------------
        # 최종 ROI 사각형 표시
        # -------------------------------------------------

        img = img_copy.copy()

        cv2.rectangle(
            img,
            (x1, y1),
            (x2, y2),
            (0, 0, 255),           # 빨간색 사각형
            2
        )

        # ROI 정보 출력
        print("ROI 선택 완료!")
        print(f"좌표: ({x1}, {y1}) ~ ({x2}, {y2})")
        print(f"크기: {x2-x1} x {y2-y1}")
        
        # ROI를 별도 창에 표시
        if roi is not None and roi.size > 0:
            cv2.imshow('Selected ROI', roi)


# ---------------------------------------------------------
# 메인 함수
# ---------------------------------------------------------

def main():

    global img, img_copy, roi

    # -------------------------------------------------
    # 현재 파이썬 파일이 있는 폴더 경로 얻기
    # -------------------------------------------------
    current_dir = os.path.dirname(os.path.abspath(__file__))

    # -------------------------------------------------
    # 사용할 수 있는 이미지 목록
    # -------------------------------------------------
    image_names = [
        'girl_laughing.jpg',
        'soccer.jpg',
        'image_gray.jpg'
    ]

    img_path = None

    # 폴더 안에서 존재하는 이미지 찾기
    for img_name in image_names:

        potential_path = os.path.join(current_dir, img_name)

        # 파일이 실제로 존재하는지 확인
        if os.path.exists(potential_path):
            img_path = potential_path
            break

    # 이미지가 하나도 없을 경우
    if img_path is None:

        print("사용 가능한 이미지가 없습니다.")
        print(f"다음 파일 중 하나를 {current_dir}에 저장하세요:")

        for name in image_names:
            print(f" - {name}")

        return

    # -------------------------------------------------
    # 이미지 불러오기
    # -------------------------------------------------
    img = cv2.imread(img_path)

    # 이미지 로딩 실패
    if img is None:
        print("이미지를 불러올 수 없습니다.")
        return

    # -------------------------------------------------
    # 이미지 크기 조정
    # -------------------------------------------------
    # 이미지가 너무 크면 화면에 다 안 보이므로
    # 적절한 크기로 줄여준다

    if img.shape[0] > 600 or img.shape[1] > 800:

        # 축소 비율 계산
        scale = min(600 / img.shape[0], 800 / img.shape[1])

        img = cv2.resize(
            img,
            (int(img.shape[1] * scale), int(img.shape[0] * scale))
        )

    # 원본 이미지 복사 (리셋용)
    img_copy = img.copy()

    # -------------------------------------------------
    # OpenCV 창 생성
    # -------------------------------------------------

    window_name = 'ROI Extraction - 마우스로 영역 선택'

    cv2.namedWindow(window_name)

    # 마우스 이벤트 함수 연결
    cv2.setMouseCallback(window_name, mouse_event)

    # -------------------------------------------------
    # 사용자 안내
    # -------------------------------------------------

    print("=" * 60)
    print("ROI(Region of Interest) 추출 프로그램")
    print("=" * 60)
    print("조작 방법:")
    print(" - 마우스 드래그 : ROI 영역 선택")
    print(" - s 키 : ROI 저장")
    print(" - r 키 : 선택 취소")
    print(" - ESC : 프로그램 종료")
    print("=" * 60)

    roi_count = 0

       # -------------------------------------------------
    # 프로그램이 계속 실행되도록 하는 무한 반복 루프
    # -------------------------------------------------
    while True:

        # 현재 img 이미지를 OpenCV 창에 출력
        # window_name이라는 이름의 창에 이미지를 표시
        cv2.imshow(window_name, img)

        # 키보드 입력을 1ms 동안 대기
        # waitKey()는 키 입력이 없으면 -1 반환
        # & 0xFF는 키 값을 8비트로 정리하는 역할 (OpenCV에서 일반적으로 사용)
        key = cv2.waitKey(1) & 0xFF


        # -------------------------------------------------
        # 's' 키를 눌렀을 때 → ROI 저장
        # -------------------------------------------------
        if key == ord('s'):

            # ROI가 실제로 존재하는지 확인
            # roi is not None → ROI가 선택되었는지 확인
            # roi.size > 0 → ROI 영역이 비어있지 않은지 확인
            if roi is not None and roi.size > 0:

                # 저장할 ROI 번호 증가
                roi_count += 1

                # ROI 저장 경로 생성
                # 예: roi_1.jpg, roi_2.jpg ...
                roi_path = os.path.join(
                    current_dir,
                    f'roi_{roi_count}.jpg'
                )

                # ROI 이미지를 파일로 저장
                cv2.imwrite(roi_path, roi)

                # 저장 완료 메시지 출력
                print(f"\nROI가 '{roi_path}' 로 저장되었습니다.")

                # ROI 이미지 크기 출력
                # roi.shape = (높이, 너비, 채널)
                print(f"크기: {roi.shape[1]} x {roi.shape[0]}")

            else:
                # ROI를 선택하지 않고 저장을 누른 경우
                print("\nROI가 선택되지 않았습니다.")


        # -------------------------------------------------
        # 'r' 키를 눌렀을 때 → ROI 선택 취소
        # -------------------------------------------------
        elif key == ord('r'):

            # 원본 이미지로 다시 복원
            # img_copy는 처음 불러온 이미지 복사본
            img = img_copy.copy()

            # ROI 변수 초기화
            roi = None

            # 사용자에게 알림 출력
            print("\nROI 선택이 취소되었습니다.")


        # -------------------------------------------------
        # ESC 키를 눌렀을 때 → 프로그램 종료
        # -------------------------------------------------
        elif key == 27:

            # 프로그램 종료 메시지 출력
            print("\n프로그램 종료")

            # while 반복문 종료
            break


    # -------------------------------------------------
    # OpenCV 창 닫기
    # -------------------------------------------------
    # 프로그램이 종료될 때 모든 OpenCV 창을 닫는다
    cv2.destroyAllWindows()


# ---------------------------------------------------------
# 프로그램 시작점 (Entry Point)
# ---------------------------------------------------------

# 파이썬 파일이 직접 실행될 때만 main() 실행
# 다른 파일에서 import 하면 실행되지 않도록 하는 안전 장치

if __name__ == "__main__":
    main()